        if($category) {
            echo "<h1>Categoria: {$category->name}</h1>";
        }

        $edicts = $category->edicts()->get();

        if($edicts) {

            echo "<h1>Editais</h1>";

            foreach($edicts as $c){
                echo "<p>Descrição: {$c->description}</p>";
            }
        }
    }

